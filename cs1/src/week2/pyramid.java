package week2;
import java.util.*;
public class pyramid {
	public static void main(String[] args) {
	Scanner sc=new Scanner(System.in);
	
	System.out.println("enter the number");
	int n=sc.nextInt();
	
	for(int i=1;i<n;i++) {
			System.out.print(1);

		}
	for(int j=1;j<n+1;j++) {
		System.out.println(1);

	}
	}
}
